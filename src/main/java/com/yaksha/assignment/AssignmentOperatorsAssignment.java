package com.yaksha.assignment;

public class AssignmentOperatorsAssignment {

	public static void main(String[] args) {
		// Declare variables
		int x = 5;
		double y = 15.5;

		// Apply assignment operators
		x += 5; // x = x + 5
		y -= 5.5; // y = y - 5.5
		x *= 2; // x = x * 2
		y /= 2; // y = y / 2

		// Print the results of assignment operations
		System.out.println("Assigned x: " + x);
		System.out.println("Assigned y: " + y);
	}
}
